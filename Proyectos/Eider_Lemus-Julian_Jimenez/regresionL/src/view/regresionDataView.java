    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double trMatriz[][] = log.transpuesta(matrizX);       
        double[][] XX = log.multiplicarMatrices(trMatriz,matrizX);
        double[][] inversa = log.matrizInversa(XX);
        double[][] xTY = log.multiplicarMatrices(trMatriz,matrizY);
        double[][] beta= log.multiplicarMatrices(inversa,xTY);
        double betaTranspuesta[][] = log.transpuesta(beta);
        double[][] bXY = log.multiplicarMatrices(betaTranspuesta, xTY);
            
        
        result+="----matriz Y---- \n";
        for(int i = 0; i < matrizY.length; i++){
            for(int j = 0; j < matrizY[i].length; j++){
                result+=String.valueOf(matrizY[i][j]+"  ");
            }
            result+="\n";
        }
        
        result+="----matriz X---- (metodo matricial: agrega columna de valor 1) \n";
        for(int i = 0; i < matrizX.length; i++){
            for(int j = 0; j < matrizX[i].length; j++){
                result+=String.valueOf(matrizX[i][j]+"  ");
            }
            result+="\n";
        }
        
        
        result+="----X transpuesta---- \n";
        for(int i = 0; i < trMatriz.length; i++){
            for(int j = 0; j < trMatriz[i].length; j++){
               result+=String.valueOf(trMatriz[i][j]+"  ");
            }
            result+=("\n");
        }
        
        result+="------(X'X)------- \n";
        for(int i = 0; i < XX.length; i++){
            for(int j = 0; j < XX[i].length; j++){
                result+=String.valueOf(XX[i][j]+"  ");
            }
            result+="\n";
        }
        
        result+="***** (X'X)^-1 ***** \n";
        for(int i = 0; i < inversa.length; i++){
            for(int j = 0; j < inversa[i].length; j++){
                result+=String.valueOf(inversa[i][j]+"  ");
            }
            result+="\n";
        }
        
        result+="***** (X'y) *****\n";
        for(int i = 0; i < xTY.length; i++){
            for(int j = 0; j < xTY[i].length; j++){
                result+=String.valueOf(xTY[i][j]+"  ");
            }
            result+="\n";
        }
        result+="***** (beta) *****\n";
        for(int i = 0; i < beta.length; i++){
            for(int j = 0; j < beta[i].length; j++){
                result+=String.valueOf(beta[i][j]+"  ");
            }
            result+="\n";
        }
        result += "Modelo de regresion:";
        String modelo ="b = ";
        for(int i = 0; i < beta.length; i++){
            for(int j = 0; j < beta[i].length; j++){
                if(i==0){
                modelo+=String.valueOf(beta[i][j])+" ";
                }else{
                    if(beta[i][j]>0){
                    modelo+=" + "+String.valueOf(beta[i][j])+"(X"+(i)+")";
                    }else{
                    modelo+=String.valueOf(beta[i][j])+"(X"+(i)+")";
                    }
                }
            }
        }
        
        result+=" \n"+modelo;
        
        double ssr=0;
        double sst=0;
        double sumatoria=0;
        for(int i = 0; i < matrizY.length; i++){
            for(int j = 0; j < matrizY[i].length; j++){
                sumatoria +=matrizY[i][j];
            }
        }
        sumatoria = Math.pow(sumatoria,2)/matrizY.length;
        ssr = bXY[0][0] - sumatoria;
        double[][] ytranspuesta= logica.transpuesta(matrizY);
        double[][] yy = logica.multiplicarMatrices(ytranspuesta, matrizY);
        sst = yy[0][0] - sumatoria;
        
        double R2 = ssr/sst;
        
        
        result+="\n SSR :"+String.valueOf(ssr);
        result+="\n SSR :"+String.valueOf(sst);
        result+="\n R^2 :"+String.valueOf(R2);
        
        
        
        log.setResultado(result);
        resultadoView resultado = new resultadoView();
        resultado.setVisible(true);
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton3ActionPerformed

